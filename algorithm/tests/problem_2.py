class Solution:
    def maxKelements(self, nums: List[int], k: int) -> int:

        heapq.heapify([-num for num in nums])
        ans = 0
        for _ in range(k):
            x = heapq.heappop(nums)
            ans += x
            heapq.heappush(nums, math.ceil(x/3))
        return ans


