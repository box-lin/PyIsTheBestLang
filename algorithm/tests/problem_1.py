class MinStack:

    def __init__(self):
        self.stack = [-1]
        self.min_stack = [float("inf")]

    def push(self, val: int) -> None:
        self.stack.append(val)
        a = self.min_stack[-1]
        a =a  if a < val else val
        self.min_stack.append(a)

    def pop(self) -> None:
        self.stack.pop()
        self.min_stack.pop()

    def top(self) -> int:
        return self.stack[-1]

    def getMin(self) -> int:
        return self.min_stack[-1]

